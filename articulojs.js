const artsWrapper = document.querySelector('.articulos-wrapper');
const urlInfo = new URLSearchParams(window.location.search);
const artId = urlInfo.get('id');
if (artsWrapper) {
  displayArticles(articles);
} else if (artId) {
  showArticle(articles);
}

function displayArticles(arts) {
  let artsBox = arts.map((art) => {
    return `<div class="articulos-item-wrapper">
        <a href="articulo.html?id=${art.id}" class="articulos-links d-flex-wrapper">
          <div class="articulo-numero-wrapper d-flex-wrapper">${art.id}</div>
          <div class="articulo-texto-wrapper">
            <h3 class="articulo-texto-title">${art.title}</h3>
            <span class="articulo-fecha-texto">Publicado: ${art.date}</span>
          </div>
        </a>
      </div>`;
  });
  artsBox = artsBox.join('');
  artsWrapper.innerHTML = artsBox;
}

function showArticle(arts) {
  arts.map((art) => {
    if (art.id == artId) {
      const artWrapper = document.querySelector('.articulo-item');
      artWrapper.innerHTML = `<h2 class="articulo-title">${art.title}</h2>` + art.content;
    }
  })
}
